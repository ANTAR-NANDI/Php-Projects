           <h5 style="color: blue">Major Illness/Surgery</h5>
           <p>Persons with the following conditions are not eligible for blood donation:</p>
           <ul>
                <li>Diseases of the heart or lungs (Donors who are asthmatic and without symptoms of asthma is eligible)</li>
                <li>High blood pressure on medication</li>
                <li>Diabetes on medication</li>
                <li>Major surgery (can donate after 12 months)</li>
                <li>AIDS or symptoms of AIDS, such as unexplained fevers, severe night sweats, unexpected weight loss, swollen glands, chronic diarrhoea or rare cancer</li>
                <li>Close contact with Hepatitis B, wait 12 months and after full course of hepatitis B vaccination (and shown a satisfactory antibody response)</li>
                <li>Infectious Diseases e.g. Chickenpox, Measles, Dengue, wait 6 months after recovery</li>
                



           </ul>
        
          <h5 style="color: blue">Some Common Reasons For Temporary Deferral</h5>
           <ul>
                <li>Cold, flu or sore throat, please wait <strong>one week</strong> after recovery or treatment</li>
                <li>Tooth extraction or dental work, please wait 3 days after treatment</li>
                <li>Skin infections (minor), wait 1 week after complete healing</li>
                <li>Normal pregnancy, please wait 6 weeks after delivery and when you are not breast-feeding.</li>
                <li>Travelled to a malaria endemic area such as rural areas in Malaysia, Thailand, Indonesia, Philippines, India, etc, please wait 6 weeks to 3 years and ask the attending doctor/nurse whether you are eligible for donation</li>
                <li>Epilepsy</li>
                <li>Hepatitis B or C</li>
                <li>Syphilis</li>



           </ul>

